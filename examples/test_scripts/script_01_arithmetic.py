def add(a: int, b: int) -> int:
    """Add two integers."""
    return a + b

def subtract(a: int, b: int) -> int:
    """Subtract b from a."""
    return a - b

def multiply(x: int, y: int) -> int:
    """Multiply two integers."""
    return x * y

def divide(a: int, b: int) -> int:
    """Integer division."""
    return a // b

def modulo(a: int, b: int) -> int:
    """Remainder of division."""
    return a % b
